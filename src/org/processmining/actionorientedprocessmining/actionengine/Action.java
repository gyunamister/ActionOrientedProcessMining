package org.processmining.actionorientedprocessmining.actionengine;

import java.util.List;

import org.processmining.actionorientedprocessmining.event.TimeMoment;

public class Action {
	public ActionFormula af;
	public List<TimeMoment> TM;
	
	public Action(ActionFormula af, List<TimeMoment> TM) {
		this.af = af;
		this.TM = TM;
	}
	
	public ActionFormula getAF() {
		return this.af;
	}
	
	public List<TimeMoment> getTM(){
		return this.TM;
	}
}
