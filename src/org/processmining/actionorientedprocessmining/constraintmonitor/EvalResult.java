package org.processmining.actionorientedprocessmining.constraintmonitor;

public class EvalResult {
	public Context ctx;
	public String outc;
	
	public EvalResult(Context ctx, String outc) {
		this.ctx = ctx;
		this.outc=outc;
	}
	
	public Context getContext() {
		return this.ctx;
	}
	
	public String getOutcome() {
		return this.outc;
	}
}
