package org.processmining.actionorientedprocessmining.event;

public class TimeWindow {
	public int start;
	public int end;
	
	public TimeWindow(int start, int end) {
		this.start = start;
		this.end = end;
	}
	
	public int getStart() {
		return this.start;
	}
	
	public int getEnd() {
		return this.end;
	}
	
	public String toString() {
		return this.start + " to " + this.end; 
	}
}
